                <div class="section mb-4">
                    <div class="h4">Universal Design</div>
                    <div class="p">Our website is designed with universal design principles to ensure accessibility for people with various limitations. Here are some features enhancing accessibility for individuals with limitations:</div>

                    <ul>
                        <li><strong>Screenreader for blind individuals:</strong> Our website is designed to be compatible with screen readers, allowing blind users to experience the content through audio cues.</li>
                        <li><strong>Color-blind individuals:</strong> Adjust color filters in accessibility settings of your operator system to view the website and test it using color-blind mode.</li>
                        <li><strong>Adjustable font size:</strong> Users can customize the font size based on their preferences in the browser settings.</li>
                        <li><strong>Only Keyboard function:</strong> Our website supports navigation and interaction using only the keyboard, ensuring compatibility for users who rely on keyboard input.</li>
                        <li><strong>Images sized adequately:</strong> Images on our website are designed to be sufficiently large, improving visibility and understanding for all users.</li>
                        <li><strong>Three-color pattern:</strong> The website employs a three-color pattern, simplifying the visual experience for users with certain cognitive or visual impairments.</li>
                        <li><strong>Breadcrumb links:</strong> Page links utilize breadcrumbs (e.g., .../...) for easy navigation and understanding of the website structure.</li>
                    </ul>
